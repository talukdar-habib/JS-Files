function isOdd(number){
    if(number % 2 === 1){
        return true;
    }
    else{
        return false;
    }
    // function use kore if else diye bijor/odd shonkha evabe kivabe korte hoy and return o evabe korte hoy
}

console.log(isOdd(54));
